class InMemoryRateLimiter:
    """Simple per-client fixed-window rate limiter for write endpoints."""

    def __init__(self, window_seconds: int = 60) -> None:
        """Initialize a fixed-window limiter with in-memory request timestamps."""

        self.window_seconds = window_seconds
        self._events: dict[str, list[float]] = {}
        self._lock = Lock()

    def allow(self, key: str, limit: int) -> bool:
        """Return True when request is within rate limit for provided client key."""

        now = monotonic()
        window_start = now - self.window_seconds

        with self._lock:
            timeline = [timestamp for timestamp in self._events.get(key, []) if timestamp >= window_start]
            if len(timeline) >= limit:
                self._events[key] = timeline
                return False
            timeline.append(now)
            self._events[key] = timeline
        return True

    def clear(self) -> None:
        """Reset limiter state; intended for tests and controlled maintenance."""

        with self._lock:
            self._events.clear()


