def _parse_int_env(name: str, default: int, min_value: int, max_value: int) -> int:
    """Parse and range-check integer environment variables."""

    raw = os.getenv(name)
    if raw is None or not raw.strip():
        return default

    try:
        value = int(raw.strip())
    except ValueError as exc:
        raise RuntimeError(f"Invalid integer value for {name}: {raw}") from exc

    if value < min_value or value > max_value:
        raise RuntimeError(
            f"{name} must be between {min_value} and {max_value}; received {value}"
        )
    return value


