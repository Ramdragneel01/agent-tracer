@app.middleware("http")
async def add_request_metadata(request: Request, call_next: Any) -> Response:
    """Attach request identifiers, security headers, and request logs."""

    request_id = request.headers.get("X-Request-ID") or str(uuid4())
    started = monotonic()

    try:
        response = await call_next(request)
    except Exception:
        elapsed_ms = (monotonic() - started) * 1000.0
        logger.exception(
            "Unhandled request failure method=%s path=%s duration_ms=%.2f request_id=%s",
            request.method,
            request.url.path,
            elapsed_ms,
            request_id,
        )
        raise

    elapsed_ms = (monotonic() - started) * 1000.0

    response.headers["X-Request-ID"] = request_id
    response.headers["X-Frame-Options"] = "DENY"
    response.headers["X-Content-Type-Options"] = "nosniff"
    response.headers["Referrer-Policy"] = "no-referrer"
    response.headers["Permissions-Policy"] = "camera=(), microphone=(), geolocation=()"
    response.headers["Content-Security-Policy"] = (
        "default-src 'none'; frame-ancestors 'none'; base-uri 'none'; form-action 'none'"
    )
    response.headers["Cache-Control"] = "no-store"
    response.headers["Pragma"] = "no-cache"

    if settings.hsts_seconds > 0:
        response.headers["Strict-Transport-Security"] = f"max-age={settings.hsts_seconds}; includeSubDomains"

    logger.info(
        "Request completed method=%s path=%s status=%s duration_ms=%.2f request_id=%s",
        request.method,
        request.url.path,
        response.status_code,
        elapsed_ms,
        request_id,
    )
    return response


